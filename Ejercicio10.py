'''
 Escribe un programa que determine el día de la semana correspondiente a un 
número ingresado por el usuario (1 para lunes, 2 para martes, etc.).
'''
n = int (input("Ingresa un numero:"))
if n == 1:
  print("Lunes")
elif n ==2:
  print("Martes")
elif n ==3:
  print("Miercoles")
elif n ==4:
  print("Jueves")
elif n ==5:
  print("Viernes")
elif n ==6:
  print("Sabado")
elif n ==7:
  print("Domingo")